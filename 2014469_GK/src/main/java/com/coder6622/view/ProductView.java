    private boolean validateProductData(String name, int quantity) {
        if (name.length() == 0 || quantity == 0) {
            return false;
        }

        return true;
    }

    public Product getProductData() {

        String maSP = txtId.getText();

        String name = txtTen.getText();

        String dvt = jcboDvt.getSelectedItem().toString();

        int giaBan = 0;

        try {
            giaBan = Integer.parseInt(txtDonGia.getText());
        } catch (NumberFormatException ex) {
        }
        String nhaCungCap = txtNhaCungCap.getText();

//        if (validateProductData(name, quantity) == false) {
//            showMessage("Thông tin không hợp lệ!");
//            return null;
//        }
        Product product = new Product(maSP, name, dvt, giaBan, nhaCungCap);

        return product;
    }


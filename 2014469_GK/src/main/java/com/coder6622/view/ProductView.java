    public void fillInputForm() {
        int row = jtableProduct.getSelectedRow();

        if (row >= 0) {
            txtId.setText(jtableProduct.getValueAt(row, 0).toString());
            txtTen.setText(jtableProduct.getValueAt(row, 1).toString());
            jcboDvt.setSelectedItem(jtableProduct.getValueAt(row, 2).toString());
            txtDonGia.setText(jtableProduct.getValueAt(row, 3).toString());
            txtNhaCungCap.setText(jtableProduct.getValueAt(row, 4).toString());

        }
    }

